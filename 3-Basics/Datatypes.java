public class Datatypes {
    public static void main(String[] args) {
        // data types primitive - inbuilt & non primitive
        // non primitives strings array
        int age = 22;
        float pie = 3.14f;
        double doublenum = 4.9d;
        boolean istrue = true; // false
        char a = 'a'; // single characters
        long longtest = 0L;// 990090909090
        short shortnum = 2;
        // non primitive datatypes
        String  stringname = "hello this is a non primitive";
        System.out.println(stringname);
    }
}
