public class ConditionalStatements {
    public static void main(String[] args) {
        int age = 20;
        if (age >= 18) {
            System.out.println("you can vote");
        } else if (age < 18) {
            System.out.println("you can`t vote");
        } else {
            System.out.println("unknown");

        }
        // &&-and ||-or
    }
}
